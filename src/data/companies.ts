export function findCompany(query: string): Company {
  const q = query.trim().toLowerCase();
  if (!q) return companies[0];
  const hit = companies.find(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.ticker.toLowerCase().includes(q) ||
      c.id.toLowerCase().includes(q),
  );
  if (hit) return hit;
  // For any unknown query, return the first company but swap the display name
  // so it feels "dynamic" while we mock-back the numbers.
  return {
    ...companies[0],
    id: `mock-${q}`,
    name: titleCase(query.trim()),
    ticker: query.trim().toUpperCase().replace(/\s+/g, "").slice(0, 10),
  };
}

function titleCase(s: string) {
  return s
    .split(/\s+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join(" ");
}
